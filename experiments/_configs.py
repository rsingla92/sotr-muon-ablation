"""Typed configuration dataclasses for Phase 2+ experiments.

A run is a Python module under ``experiments/configs/`` that exposes a single
top-level ``config`` attribute of type :class:`RunConfig`. The training entry
point (:mod:`experiments.train`) takes ``--config <dotted.module.path>``,
imports it, and reads ``module.config``.

This is the modded-nanogpt convention (Python files as configs, not YAML);
see ``docs/EXPERIMENTS.md`` and ``CONTRIBUTING.md`` §"Configs" for rationale.

Public API:

- :class:`RunConfig` — full per-run config, mirrors upstream
  ``Hyperparameters`` plus optimizer choice, reproducibility, and
  logging / stability sub-configs.
- :class:`OptimizerKind` — enum of supported optimizer dispatch keys.
- :class:`UpstreamHparams` — duck-typed object exposing the field names
  upstream's ``train_gpt2.py`` reads from ``args``. Built by
  :meth:`RunConfig.to_upstream_hparams`.
"""

from __future__ import annotations

from dataclasses import asdict, dataclass, field, fields
from enum import Enum
from pathlib import Path
from typing import Any

__all__ = [
    "OptimizerKind",
    "RunConfig",
    "UpstreamHparams",
]


class OptimizerKind(str, Enum):
    """Supported optimizer dispatch keys. See PROTOCOL §6 for the locked baselines."""

    ADAMW = "adamw"
    LION = "lion"
    MUON = "muon"
    SOTR = "sotr"

    @classmethod
    def from_str(cls, s: str) -> OptimizerKind:
        try:
            return cls(s.lower())
        except ValueError as e:
            valid = ", ".join(k.value for k in cls)
            raise ValueError(f"unknown optimizer {s!r}; valid: {valid}") from e


@dataclass(frozen=True)
class UpstreamHparams:
    """Duck-typed mirror of upstream ``Hyperparameters`` (train_gpt2.py:336–355).

    The vendored ``experiments/train.py`` reads ``args.input_bin``, ``args.batch_size``
    etc. throughout — to keep the rest of the upstream script unchanged, we hand
    it an object that exposes those exact attribute names.
    """

    input_bin: str = "external/modded-nanogpt/data/fineweb10B/fineweb_train_*.bin"
    input_val_bin: str = "external/modded-nanogpt/data/fineweb10B/fineweb_val_*.bin"
    batch_size: int = 8 * 64
    device_batch_size: int = 64
    sequence_length: int = 1024
    num_iterations: int = 5100
    embed_learning_rate: float = 0.0036
    muon_learning_rate: float = 0.02
    warmup_iters: int = 0
    warmdown_iters: int = 1450
    weight_decay: float = 0.0
    val_loss_every: int = 125
    val_tokens: int = 10485760
    save_every: int = 0


@dataclass(frozen=True)
class RunConfig:
    """Top-level config for a single run.

    Phase-2 ablations populate this from a generator (see
    ``experiments/scripts/gen_phase2_configs.py``); single one-off configs
    are written by hand in ``experiments/configs/<name>.py``.

    The ``hparams`` block is what ``train.py``'s upstream-derived top-level
    code reads (it expects a ``args.input_bin``-style API). The remaining
    fields drive optimizer dispatch, reproducibility, and logging.
    """

    # Identity / categorization
    name: str
    purpose: str  # one of {"sanity", "phase1", "phase2", "phase3", "phase4"}
    description: str = ""

    # Upstream-mirroring data + schedule + iteration hparams.
    hparams: UpstreamHparams = field(default_factory=UpstreamHparams)

    # Optimizer dispatch
    optimizer_name: OptimizerKind = OptimizerKind.MUON

    # Optimizer-specific knobs (mostly ignored unless the matching name is selected).
    # We keep the universal `momentum` here because Muon and SOTR both use it.
    momentum: float = 0.95

    # SOTR-only knobs. Ignored unless optimizer_name == SOTR.
    sotr_alpha: float = 0.5
    sotr_delta: float = 1.0
    sotr_ns_steps: int = 2

    # Lion-only knobs. Ignored unless optimizer_name == LION.
    lion_betas: tuple[float, float] = (0.9, 0.99)

    # Reproducibility
    seed: int = 0
    deterministic: bool = True

    # Logging and stability monitoring (PROTOCOL §8, §12)
    log_every_steps: int = 50
    rolling_window: int = 100
    grad_window: int = 1000

    # Run-id override. None → auto-generated by experiments._run_id.
    run_id: str | None = None

    # Where to write outputs. Default lands under repo's results/.
    output_root: str = "results"

    # ----------------------------------------------------------------
    # Validation
    # ----------------------------------------------------------------
    def __post_init__(self) -> None:
        valid_purposes = {"sanity", "phase1", "phase2", "phase3", "phase4"}
        if self.purpose not in valid_purposes:
            raise ValueError(f"purpose={self.purpose!r}; must be one of {sorted(valid_purposes)}")
        if not isinstance(self.optimizer_name, OptimizerKind):
            # Allow string values for ergonomics in handwritten configs.
            object.__setattr__(
                self, "optimizer_name", OptimizerKind.from_str(str(self.optimizer_name))
            )
        if not 0.0 < self.momentum < 1.0:
            raise ValueError(f"momentum must be in (0, 1); got {self.momentum}")
        if self.optimizer_name == OptimizerKind.SOTR:
            if not 0.0 <= self.sotr_alpha <= 1.0:
                raise ValueError(f"sotr_alpha must be in [0, 1]; got {self.sotr_alpha}")
            if self.sotr_delta <= 0:
                raise ValueError(
                    f"sotr_delta must be > 0 (use float('inf') to disable); got {self.sotr_delta}"
                )
            if self.sotr_ns_steps < 0:
                raise ValueError(f"sotr_ns_steps must be >= 0; got {self.sotr_ns_steps}")
        if self.hparams.batch_size % self.hparams.device_batch_size != 0:
            raise ValueError(
                "hparams.batch_size must be a multiple of device_batch_size; got "
                f"{self.hparams.batch_size} / {self.hparams.device_batch_size}"
            )

    # ----------------------------------------------------------------
    # Helpers
    # ----------------------------------------------------------------
    def to_upstream_hparams(self) -> UpstreamHparams:
        """Return the upstream-compatible Hyperparameters dataclass.

        The vendored ``train.py`` does ``args = cfg.to_upstream_hparams()`` and
        all subsequent ``args.input_bin`` / ``args.muon_learning_rate`` reads
        keep working unchanged.
        """
        return self.hparams

    def primary_lr(self) -> float:
        """Learning rate for the primary (transformer.h.*) optimizer.

        Upstream stores this as ``muon_learning_rate``; we reuse the field
        verbatim for all optimizer choices to keep one tunable surface.
        """
        return self.hparams.muon_learning_rate

    def aux_lr(self) -> float:
        """Learning rate for the aux AdamW (lm_head/embed)."""
        return self.hparams.embed_learning_rate

    def to_dict(self) -> dict[str, Any]:
        """JSON-serializable dict snapshot. For run_id hashing and provenance dumps."""
        d = asdict(self)
        # Enums → their string value.
        d["optimizer_name"] = self.optimizer_name.value
        return d

    def hash_input(self) -> str:
        """Deterministic short string for run-id seeding. Excludes the run_id field itself."""
        d = self.to_dict()
        d.pop("run_id", None)
        # Sort keys so the hash is stable across Python releases.
        return repr(sorted(d.items()))

    def fields_summary(self) -> str:
        """One-line summary of the most-likely-to-vary fields. Useful in logs."""
        h = self.hparams
        return (
            f"name={self.name} optim={self.optimizer_name.value} "
            f"lr_h={h.muon_learning_rate} lr_e={h.embed_learning_rate} "
            f"iters={h.num_iterations} seed={self.seed}"
        )

    @classmethod
    def field_names(cls) -> list[str]:
        return [f.name for f in fields(cls)]

    def relative_output_dir(self, run_id: str) -> Path:
        """Where this run's artifacts land, relative to repo root."""
        return Path(self.output_root) / self.purpose / run_id
